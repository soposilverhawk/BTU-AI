// Normalize input (dashes, pipes, spacing)
const cleanedRawData = rawData
  .replace(/\u2013|\u2014|\u2012|\u2011|\u2010/g, "-") // Replace all dash types with -
  .replace(/\s*-\s*/g, " - ")                         // Normalize spacing around dashes
  .replace(/\s*\|\s*/g, " | ")                        // Normalize spacing around pipes
  .trim();

export const studentAchievements = cleanedRawData
  .split("\n")
  .map((line, index) => {
    if (!line.includes("-") || !line.includes("|")) {
      console.warn(`❌ Skipping malformed line ${index}:`, line);
      return null;
    }

    const [namePart, rest] = line.split(" - ").map((part) => part.trim());
    const [positionPart, companyPart] = rest.split(" | ").map((part) => part.trim());

    if (!namePart || !positionPart || !companyPart) {
      console.warn(`⚠️ Missing parts in line ${index}:`, { namePart, positionPart, companyPart });
      return null;
    }

    return new StudentAchievement(namePart, positionPart, companyPart);
  })
  .filter(Boolean);

