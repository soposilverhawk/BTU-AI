// Helper to detect LinkedIn links reliably
const isLinkedInUrl = (str) =>
  str?.startsWith("https://www.linkedin.com/in/");

export const studentAchievements = rawData
  .replace(/\u2013|\u2014|\u2012|\u2011|\u2010/g, "-") // normalize dashes
  .split("\n")
  .map((line, index) => {
    const [namePart, rest] = line.split(" - ").map((part) => part.trim());

    if (!namePart || !rest) {
      console.warn(`⚠️ Skipping malformed line ${index}:`, line);
      return null;
    }

    const parts = rest.split("|").map((p) => p.trim());

    let position = "";
    let company = "";
    let linkedIn = null;

    if (parts.length === 3) {
      // Full line with LinkedIn
      [position, company, linkedIn] = parts;
      if (!isLinkedInUrl(linkedIn)) linkedIn = null;
    } else if (parts.length === 2) {
      // No LinkedIn
      [position, company] = parts;
    } else if (parts.length === 1) {
      // Unusual format
      position = parts[0];
    } else if (parts.length > 3) {
      // Maybe LinkedIn is part of the middle accidentally
      position = parts[0];
      company = parts[1];
      const possibleLinkedIn = parts.find((part) => isLinkedInUrl(part));
      if (possibleLinkedIn) linkedIn = possibleLinkedIn;
    }

    return new StudentAchievement(namePart, position, company, linkedIn);
  })
  .filter(Boolean);

